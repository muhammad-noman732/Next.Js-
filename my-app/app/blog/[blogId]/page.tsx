type PageProps = {
  params: {
    blogId: string;
  };
  searchParams?: {
    [key: string]: string | string[] | undefined;
  };
};

export async function generateMetadata({ params }: PageProps) {
  const { blogId } = await params;

  return {
    title: `Blog ${blogId}`,
    description: `Blog page for post ${blogId}`,
  };
}

