interface Todo {
  id: number;
  userId: number;
  title: string;
  completed: boolean;
}

export async function generateStaticParams() {
  const response = await fetch("https://jsonplaceholder.typicode.com/todos");

  const data: Todo[] = await response.json();

  return data.map((item) => ({
    blogId: item.id.toString(),
  }));
}

